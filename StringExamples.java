class StringExamples {
    public static void main(String[] args) {
    // trim
    String text = " Credo Systemz ";
    System.out.println(text);
    System.out.println(text.trim());
    // equals
    String myname = "AjitKumar";
    String surname = "Kumar";
    String fullname = "AjitKumar";
    System.out.println(myname.equals(surname));
    System.out.println(myname.equals(fullname));
    // charAt,length,toUpperCase,toLowerCase
    String Name = "Credo Systemz";
    System.out.println(Name.charAt(3));
    System.out.println(Name.length());
    System.out.println(Name.toUpperCase());
    System.out.println(Name.toLowerCase());
    }
    
}
